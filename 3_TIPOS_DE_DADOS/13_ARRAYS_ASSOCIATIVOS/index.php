<?php

    /*
        ATENÇÃO: Um array associativo é literalmente um array contendo uma chave e valor!
            Exemplo: $arrayAssociativo = ["nome" => "Carlos", "idade" => 17]

        OBS: Um array associativo aceita todos os tipos de dados também...
    */

    $arrayAssociative = ["name" => "Judite", "age" => 2, "color" => "black"];

    echo $arrayAssociative["name"];
    echo "<br>";

    echo $arrayAssociative["age"];
    echo "<br>";

    echo $arrayAssociative["color"];
    echo "<br>";