<?php

    // +=
    $a = 0; // $a = 10;
    $a += 10; // $a = $value2 + 20;

    echo $a . "<br>"; // 10

    // -=
    $b = 0;
    $b -= 5; // $b = $b - 5;

    echo $b . "<br>"; // -5

    // *=
    $c = 1;
    $c *= 5; // $c = $c * 5;

    echo $c . "<br>"; // 5

    // /=
    $d = 10;
    $d /= 2; // $d = $d / 2;

    echo $d . "<br>"; // 5

    // %=
    $e = 10;
    $e %= 3; // $e = $e % 3;

    echo $e . "<br>"; // 1